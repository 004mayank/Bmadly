#!/usr/bin/env node

// Real-ish BMAD runner (MVP): installs BMAD Method into /work, then runs a
// minimal quick-dev style generation that outputs a runnable Next.js app into
// /work/app.
//
// IMPORTANT:
// - BMAD Method is primarily an installer + workflow/skill bundles intended for
//   AI IDE execution. This runner bridges that gap by materializing _bmad/ and
//   then generating an app with a deterministic template.
// - We still use the platform contract:
//   - emit incremental logs
//   - write app into /work/app
//   - print final JSON prefixed with [output]
//
// Env inputs (passed by backend):
// - BMAD_PROVIDER, BMAD_MODEL, BMAD_API_KEY
// - BMAD_IDEA
// - BMAD_PLAN_JSON

const { execSync } = require("node:child_process");
const fs = require("node:fs");
const path = require("node:path");

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

function sh(cmd, opts = {}) {
  return execSync(cmd, { stdio: "pipe", env: process.env, ...opts }).toString("utf8");
}

function ensureDir(p) {
  fs.mkdirSync(p, { recursive: true });
}

function writeFile(p, content) {
  ensureDir(path.dirname(p));
  fs.writeFileSync(p, content);
}

async function main() {
  const provider = process.env.BMAD_PROVIDER || "openai";
  const model = process.env.BMAD_MODEL || "gpt-4o-mini";
  const hasKey = Boolean(process.env.BMAD_API_KEY);

  const idea = (process.env.BMAD_IDEA || "").trim();
  let plan = {};
  try {
    plan = JSON.parse(process.env.BMAD_PLAN_JSON || "{}");
  } catch {
    plan = { raw: process.env.BMAD_PLAN_JSON };
  }

  const workDir = "/work";
  const bmadDir = path.join(workDir, "_bmad");
  const outDir = path.join(workDir, "_bmad-output");
  const appDir = path.join(workDir, "app");

  ensureDir(workDir);

  console.log(`[bmad] runner=real-bmad-quick-dev (mvp)`);
  await sleep(150);
  console.log(`[bmad] provider=${provider} model=${model} apiKey=${hasKey ? "present" : "missing"}`);
  await sleep(150);

  // 1) Install BMAD bundle if not present.
  if (!fs.existsSync(bmadDir) || !fs.existsSync(path.join(bmadDir, "_config"))) {
    console.log(`[bmad] installing bmad-method bundle into ${bmadDir}…`);
    await sleep(150);
    try {
      // Non-interactive install.
      // Note: tools none -> only creates _bmad assets without IDE config.
      sh(`bmad-method install --directory ${workDir} --modules bmm --tools none --yes`);
      console.log(`[bmad] install complete`);
    } catch (e) {
      console.error(`[bmad] install failed: ${String(e?.message || e)}`);
      process.exit(1);
    }
  } else {
    console.log(`[bmad] bundle already present, skipping install`);
  }

  // 2) Minimal quick-dev generation.
  // For true BMAD parity, we would interpret _bmad/**/workflow.md + step md files
  // and run them via an LLM. This MVP focuses on wiring BMAD into the container
  // and producing a working preview app.
  console.log(`[bmad] quick-dev: generating Next.js app into ${appDir}…`);
  await sleep(150);

  ensureDir(appDir);
  ensureDir(path.join(appDir, "app"));

  const features = Array.isArray(plan?.features) ? plan.features : [];
  const featureList = features.length ? features.map((f) => `- ${String(f)}`).join("\n") : "- (none)";

  writeFile(
    path.join(appDir, "package.json"),
    JSON.stringify(
      {
        name: "bmadly-generated-app",
        private: true,
        version: "0.0.0",
        scripts: {
          dev: "next dev --hostname 0.0.0.0 --port 3000",
          build: "next build"
        },
        dependencies: {
          next: "15.5.15",
          react: "19.1.0",
          "react-dom": "19.1.0"
        }
      },
      null,
      2
    )
  );

  writeFile(
    path.join(appDir, "next.config.js"),
    `/** @type {import('next').NextConfig} */\nmodule.exports = {};\n`
  );

  writeFile(
    path.join(appDir, "app", "layout.tsx"),
    `export const metadata = { title: 'Bmadly Preview', description: 'Generated by BMAD (quick-dev MVP)' };\n\nexport default function RootLayout({ children }: { children: React.ReactNode }) {\n  return (\n    <html lang='en'>\n      <body style={{ margin: 0, fontFamily: 'ui-sans-serif, system-ui', background: '#f8fafc', color: '#0f172a' }}>{children}</body>\n    </html>\n  );\n}\n`
  );

  writeFile(
    path.join(appDir, "app", "page.tsx"),
    `export default function Page() {\n  return (\n    <main style={{ padding: 28, maxWidth: 920, margin: '0 auto' }}>\n      <h1 style={{ margin: 0, fontSize: 34, letterSpacing: '-0.03em' }}>Bmadly (Real BMAD Wiring – MVP)</h1>\n      <p style={{ marginTop: 8, color: '#475569' }}>BMAD bundle installed in-container. App generated via quick-dev bridge.</p>\n\n      <section style={{ marginTop: 18, padding: 16, border: '1px solid #e2e8f0', borderRadius: 14, background: '#fff' }}>\n        <div style={{ fontSize: 12, color: '#64748b' }}>Idea</div>\n        <div style={{ marginTop: 6, fontSize: 16, whiteSpace: 'pre-wrap' }}>${JSON.stringify(idea || "(no idea provided)")}</div>\n      </section>\n\n      <section style={{ marginTop: 14, padding: 16, border: '1px solid #e2e8f0', borderRadius: 14, background: '#fff' }}>\n        <div style={{ fontSize: 12, color: '#64748b' }}>Planned features</div>\n        <pre style={{ margin: '8px 0 0', fontSize: 13, whiteSpace: 'pre-wrap' }}>${JSON.stringify(featureList)}</pre>\n      </section>\n\n      <section style={{ marginTop: 14, padding: 16, border: '1px solid #e2e8f0', borderRadius: 14, background: '#fff' }}>\n        <div style={{ fontSize: 12, color: '#64748b' }}>BMAD install</div>\n        <p style={{ marginTop: 8, color: '#334155', lineHeight: 1.5 }}>\n          _bmad/ and _bmad-output/ are present in the container workspace. Next step: implement a workflow interpreter\n          that reads _bmad/**/workflow.md + steps and drives them with your selected LLM.\n        </p>\n      </section>\n    </main>\n  );\n}\n`
  );

  // optional: write a small marker artifact to _bmad-output
  ensureDir(outDir);
  writeFile(
    path.join(outDir, "planning-artifacts", "BMADLY-MVP.md"),
    `# Bmadly Real BMAD Wiring (MVP)\n\n- BMAD bundle installed into /work/_bmad\n- This run generated a Next.js app into /work/app\n- Next: interpret BMAD workflow markdown and execute steps using LLM\n`
  );

  console.log(`[bmad] installing app dependencies…`);
  await sleep(150);
  sh(`npm install --silent`, { cwd: appDir });

  console.log(`[bmad] done`);

  const output = {
    ok: true,
    kind: "real-bmad-quick-dev-mvp",
    provider,
    model,
    installed: {
      bmadDir: "/work/_bmad",
      outputDir: "/work/_bmad-output"
    },
    appDir: "/work/app"
  };

  console.log(`[output] ${JSON.stringify(output)}`);
}

main().catch((err) => {
  console.error(`[bmad] error: ${String(err?.message || err)}`);
  process.exit(1);
});
