#!/usr/bin/env node

// Mock BMAD runner that PRODUCES A REAL static Next.js export.
// This exists so the platform can implement a real preview flow (no port mapping)
// even when the real BMAD runtime isn't available in the container yet.
//
// Contract:
// - emit incremental logs
// - write a static site to /work/out
// - print final JSON prefixed with [output]

import { execSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

function sh(cmd, cwd) {
  return execSync(cmd, { cwd, stdio: "pipe", env: process.env }).toString("utf8");
}

function ensureDir(p) {
  fs.mkdirSync(p, { recursive: true });
}

async function main() {
  const provider = process.env.BMAD_PROVIDER || "openai";
  const model = process.env.BMAD_MODEL || "gpt-4o-mini";
  const hasKey = Boolean(process.env.BMAD_API_KEY);

  const idea = (process.env.BMAD_IDEA || "").trim();
  let plan = {};
  try {
    plan = JSON.parse(process.env.BMAD_PLAN_JSON || "{}");
  } catch {
    plan = { raw: process.env.BMAD_PLAN_JSON };
  }

  const workDir = "/work";
  const appDir = path.join(workDir, "app");
  const outDir = path.join(workDir, "out");

  ensureDir(workDir);

  console.log(`[bmad] starting static nextjs build (mock)`);
  await sleep(250);
  console.log(`[bmad] provider=${provider} model=${model} apiKey=${hasKey ? "present" : "missing"}`);
  await sleep(250);
  console.log(`[bmad] idea=${idea ? JSON.stringify(idea.slice(0, 140)) : "(none)"}`);
  await sleep(250);
  console.log(`[bmad] generating Next.js app skeleton…`);

  // Create a minimal Next.js app (app router) by writing files directly.
  // Avoid create-next-app to reduce network/deps; use Next at runtime via npm.
  ensureDir(appDir);
  ensureDir(path.join(appDir, "app"));

  fs.writeFileSync(
    path.join(appDir, "package.json"),
    JSON.stringify(
      {
        name: "bmadly-generated-app",
        private: true,
        version: "0.0.0",
        scripts: {
          build: "next build",
          export: "next build && next export -o out",
          start: "next start"
        },
        dependencies: {
          next: "15.5.15",
          react: "19.1.0",
          "react-dom": "19.1.0"
        }
      },
      null,
      2
    )
  );

  fs.writeFileSync(
    path.join(appDir, "next.config.js"),
    `/** @type {import('next').NextConfig} */\nmodule.exports = { output: 'export' };\n`
  );

  fs.writeFileSync(
    path.join(appDir, "app", "layout.tsx"),
    `export const metadata = { title: 'Bmadly Preview', description: 'Generated by BMAD (mock)' };\n\nexport default function RootLayout({ children }: { children: React.ReactNode }) {\n  return (\n    <html lang='en'>\n      <body style={{ margin: 0, fontFamily: 'ui-sans-serif, system-ui' }}>{children}</body>\n    </html>\n  );\n}\n`
  );

  const features = Array.isArray(plan?.features) ? plan.features : [];
  const featureList = features.length
    ? features.map((f) => `- ${String(f)}`).join("\\n")
    : "- (mock) Feature list will appear here";

  fs.writeFileSync(
    path.join(appDir, "app", "page.tsx"),
    `export default function Page() {\n  return (\n    <main style={{ padding: 28, maxWidth: 900, margin: '0 auto' }}>\n      <h1 style={{ margin: 0, fontSize: 34, letterSpacing: '-0.03em' }}>Bmadly Preview</h1>\n      <p style={{ marginTop: 8, color: '#475569' }}>Static preview generated from your idea.</p>\n\n      <div style={{ marginTop: 18, padding: 16, border: '1px solid #e2e8f0', borderRadius: 14, background: '#ffffff' }}>\n        <div style={{ fontSize: 12, color: '#64748b' }}>Idea</div>\n        <div style={{ marginTop: 6, fontSize: 16, whiteSpace: 'pre-wrap' }}>${JSON.stringify(
          idea || "(no idea provided)"
        )}</div>\n      </div>\n\n      <div style={{ marginTop: 14, padding: 16, border: '1px solid #e2e8f0', borderRadius: 14, background: '#ffffff' }}>\n        <div style={{ fontSize: 12, color: '#64748b' }}>Planned features</div>\n        <pre style={{ margin: '8px 0 0', fontSize: 13, whiteSpace: 'pre-wrap' }}>${JSON.stringify(featureList)}</pre>\n      </div>\n\n      <div style={{ marginTop: 14, padding: 16, border: '1px solid #e2e8f0', borderRadius: 14, background: '#ffffff' }}>\n        <div style={{ fontSize: 12, color: '#64748b' }}>Notes</div>\n        <p style={{ marginTop: 8, color: '#334155', lineHeight: 1.5 }}>\n          This is a mock BMAD generator that produces a real static Next.js export so Bmadly can show a live preview without running a dev server.\n          Swap to the real BMAD runtime later by changing BMAD_COMMAND and installing BMAD inside the runner image.\n        </p>\n      </div>\n    </main>\n  );\n}\n`
  );

  fs.writeFileSync(
    path.join(appDir, "app", "globals.css"),
    `html,body{background:#f8fafc;color:#0f172a}`
  );

  await sleep(350);
  console.log(`[bmad] installing dependencies…`);

  // npm install
  sh("npm install --silent", appDir);

  await sleep(350);
  console.log(`[bmad] building static export…`);

  // next export writes to appDir/out
  sh("npm run export", appDir);

  // copy export to /work/out
  ensureDir(outDir);
  const builtOut = path.join(appDir, "out");
  sh(`cp -R ${builtOut}/. ${outDir}/`, workDir);

  console.log(`[bmad] build complete`);

  const output = {
    ok: true,
    kind: "static-nextjs-export",
    outDir: "/work/out",
    provider,
    model
  };

  console.log(`[output] ${JSON.stringify(output)}`);
}

main().catch((err) => {
  console.error(`[bmad] error: ${String(err?.message || err)}`);
  process.exit(1);
});
